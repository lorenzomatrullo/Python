# Math Operators
# 
# +  (addition)
# -  (subtraction)
# *  (multiplication)
# /  (division)
# ** (exponentiation)
# %  (modulus) means "the rest of a division"

friends = 5

# friends = friends + 1
friends += 1

# friends = friends - 2
friends -= 2

# friends = friends * 3
friends *= 3

# friends = friends / 2
friends /= 2

# friends = friends ** 2
friends **= 2

remainder = friends % 2

print(friends)
print(remainder)
